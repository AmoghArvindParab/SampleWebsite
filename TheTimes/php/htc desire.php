      <h1 class="title">HTC Desire 728 review: For the brand conscious</h1>
      <p class="author">By Debashis Sarkar, The Times</p>
      <p class="date"><img src="images/clock.jpg" class="clock">Updated: Feb 27, 2016, 06.44 PM IST</p>


	   <h1><b> Taiwan's HTC is among the big smartphone players worst hit by the recent invasion by Chinese brands in India. </b></h1>
<iframe width="590" height="585" src="https://www.youtube.com/embed/T33cHP1DTe4" frameborder="0" allowfullscreen></iframe>
	    <img src="images/specifications.png" width=auto height=auto hspace="135"  > 

      <p>
        <span class="location">  While the company had been silent for sometime, the company is trying hard to be back in the game by focusing on its mid-range Desire phablet portfolio.
	   It has recently launched several Desire smartphones -- 820, 828, 728, 626, 630 and more -- in the price range of between Rs 12,000 and Rs 18,000 to gain traction.  <br><br></span><b>Display</b><br>It offers a 5.5-inch 720p HD display; HTC has <b>used a Super LCD2 capacitive touchscreen</b> on the smartphone. The viewing angles are decent, while sunlight visibility is average at best. The contrast ratio is at par with what other smartphone in the mid-range segment offer. The colour reproduction is average. Overall, some users might feel that the display lacks the punch offered by the likes of Samsung, Xiaomi, OnePlus and other.
      </p>
      <p>
      <b> HTC Desire 728 Dual SIM </b><br><br>  The 5.5-inch HTC Desire 728 Dual SIM smartphone is powered by a <b>1.3GHz octa-core MT6753 processor with 2GB RAM and Android 5.1-based HTC Sense.</b>
	  It sports a 13MP rear camera with LED flash and 5MP front-facing camera. Backed by a <b>2,800mAh battery, the handset comes with 16GB internal storage</b> and users can expand storage up to 200GB via microSD card.
<br>On the connectivity front, it <b>supports 4G, 3G, Wi-Fi, Bluetooth, and A-GPS.</b> The device also comes with BoomSound front stereo speakers.
The Desire 728 Dual SIM got a price cut soon after launch and now costs Rs 16,999.
      </p>
      <p>
        <b> Design </b><br><br>  The Desire 728 Dual SIM looks very similar to other <b>5.5-inch Desire phablets</b> The device sports a very simplistic design with a neat finishing. 
		The rear cover sports a soft plastic matte finish and is somewhat scratch proof. The battery cannot to be removed. 
		The front is mostly dominated by the screen and on the left, there are <b>two nano sim card slots, along with a separate microSD card slot. </b> The dual speaker vents are neatly placed on the front top and bottom bezels.The build quality is decent. But in terms of durability, the device needs to be handled with care.<br>
 	    Despite being a 5.5-inch smartphone,<b>it is lightweight, compact and easy to grip on one hand.</b>
      </p>
      <p>
        <b> Hardware </b> <br><br> This smartphone is well-equipped to cater to the performance demands of most average users. <b>The 1.3GHz octa-core MT6753 processor with 2GB RAM </b> takes care of heavy games and multitasking. Overall, the phone is pretty smooth without much of lags or glitches.Also,
	 <b>it gets heated up when playing games</b>  but this doesn't mar the experience.
      </p>
      <p>
        <b> Software </b><br><br> <b>  The Desire 728 runs Android 5.1.1-based HTC Sense UI.</b> There is nothing new on the software front for those who have already used a mid-range Desire smartphone. HTC's BlinkFeed still continues to be a blessing and new HTC users can certainly look up to it.
		<br>Overall, there are no glitches in the UI and experience is pleasing.<br> <b> The audio quality of the external speakers are great. It is loud and clear with a bit of thump, thanks to BoomSound.</b> 
      </p>
	  <img src="images/htc-desire.png" width="790"> 
      <p>
       <b> Camera </b> <br><br>   It mounts a <b>13MP f/2.2 , 28mm autofocus rear camera with LED Flash at the back.</b> It is capable of shooting <b>1080p videos.</b> 
	   The camera is not the fastest in its class. While the device can shoot some decent pictures in good lighting conditions, the camera disappoints in low lighting conditions.
	   Also, the quality is average at best.<br> While there is little software customizations, which is good, but the images are not vivid enough to impress. 
	   On the other hand, the 5MP, f/2.8, 34mm front camera is capable of clicking some decent selfies.
	   There is no front Flash, so, it is difficult to click selfies under low lighting.
      </p>
      <p>
        <b> Battery and connectivity </b><br><br>  The Desire 728 is powered by a <b> 2,800mAh battery.</b>  It can last a full day easily with mixed usage of Wi-Fi, 4G, 3G and a bit of gaming and video playback. 
		The battery takes a maximum of two and a half hours to get fully charged from zero with a standard charger.<b>This is a dual sim smartphone and supports nano sim cards only. </b><br>
		It supports Indian 4G bands and is powered by LTE Cat 4 modem with a maximum 150/50 Mbps speeds. There is also a <b>microSD card slot with a capacity of up to 200GB.</b> Apart from regular connectivity options, there is FM radio support and comes without NFC.
      </p>
      <p>
       <b> Verdict </b><br><br> The HTC Desire 728 Dual SIM smartphone has nothing extraordinary to attract the masses for the price of Rs 16,999.It is an average HTC phablet like other Desire smartphones. However, for those who are picky about which brand they are using, HTC gives a refreshing break for the price. After all not everyone is eager to bet on smartphones from Chinese startups with more specs for lesser price.
      </p>
